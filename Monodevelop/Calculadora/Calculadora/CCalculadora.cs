using System;
using Calculadora;
using Gtk;

public class CCalculadora {

    public float suma(float num1, float num2) {
        return num1 + num2;

    }
    public float resta(float num1, float num2)
    {
        return num1 - num2;

    }
    public float multiplpicacion(float num1, float num2)
    {
        return num1 * num2;

    }
    public float division(float num1, float num2)
    {
        return num1 / num2;

    }

}